function showArtSubjects() {
  var x = document.getElementById('artSubjects');
  if (x.style.display === 'block') {
    x.style.display = 'none';
  } else {
    x.style.display = 'block';
  }
}

function showSQOptions() {
  var x = document.getElementById('sqOptions');
  if (x.style.display === 'block') {
    x.style.display = 'none';
  } else {
    x.style.display = 'block';
  }
}
